# Assignment operations (simulated with functions)

def assign_value(x, y):
    x = y
    return x

def add_and_assign(x, y):
    x += y
    return x

def subtract_and_assign(x, y):
    x -= y
    return x

def multiply_and_assign(x, y):
    x *= y
    return x

def divide_and_assign(x, y):
    x /= y
    return x

def modulo_and_assign(x, y):
    x %= y
    return x

def bitwise_and_assign(x, y):
    x &= y
    return x

def bitwise_or_assign(x, y):
    x |= y
    return x

def bitwise_xor_assign(x, y):
    x ^= y
    return x

def left_shift_and_assign(x, shift_by):
    x <<= shift_by
    return x

def right_shift_and_assign(x, shift_by):
    x >>= shift_by
    return x

