# Bitwise operations
def bitwise_and(x, y):
    return x & y

def bitwise_or(x, y):
    return x | y

def bitwise_xor(x, y):
    return x ^ y

def bitwise_not(x):
    return ~x

def bitwise_left_shift(x, shift_by):
    return x << shift_by

def bitwise_right_shift(x, shift_by):
    return x >> shift_by